def build_sam2(
    config_file,
    ckpt_path=None,
    device="cuda",
    mode="eval",
    hydra_overrides_extra=[],
    apply_postprocessing=True,
    **kwargs,
):

    if apply_postprocessing:
        hydra_overrides_extra = hydra_overrides_extra.copy()
        hydra_overrides_extra += [
            # dynamically fall back to multi-mask if the single mask is not stable
            "++model.sam_mask_decoder_extra_args.dynamic_multimask_via_stability=true",
            "++model.sam_mask_decoder_extra_args.dynamic_multimask_stability_delta=0.05",
            "++model.sam_mask_decoder_extra_args.dynamic_multimask_stability_thresh=0.98",
        ]
    # Read config and init model
    cfg = compose(config_name=config_file, overrides=hydra_overrides_extra)
    OmegaConf.resolve(cfg)
    model = instantiate(cfg.model, _recursive_=True)
    _load_checkpoint(model, ckpt_path)
    model = model.to(device)
    if mode == "eval":
        model.eval()
    return model


def build_sam2_video_predictor(
    config_file,
    ckpt_path=None,
    device="cuda",
    mode="eval",
    hydra_overrides_extra=[],
    apply_postprocessing=True,
    vos_optimized=False,
    strict_loading=True,
    training_config_file="",
    apply_long_term_memory=False,
    apply_credible_initial_frame=True,
    num_cand_to_cond_frame=1,
    num_cifs_candidate_frame=5,
    num_long_mem_frame=3,
    **kwargs,
):
    hydra_overrides = [
        "++model._target_=sam2.sam2_video_predictor.SAM2VideoPredictor",
    ]
    if vos_optimized:
        hydra_overrides = [
            "++model._target_=sam2.sam2_video_predictor.SAM2VideoPredictorVOS",
            "++model.compile_image_encoder=True",  # Let sam2_base handle this
        ]

    if apply_postprocessing:
        hydra_overrides_extra = hydra_overrides_extra.copy()
        hydra_overrides_extra += [
            # dynamically fall back to multi-mask if the single mask is not stable
            "++model.sam_mask_decoder_extra_args.dynamic_multimask_via_stability=true",
            "++model.sam_mask_decoder_extra_args.dynamic_multimask_stability_delta=0.05",
            "++model.sam_mask_decoder_extra_args.dynamic_multimask_stability_thresh=0.98",
            # the sigmoid mask logits on interacted frames with clicks in the memory encoder so that the encoded masks are exactly as what users see from clicking
            "++model.binarize_mask_from_pts_for_mem_enc=true",
            # fill small holes in the low-res masks up to `fill_hole_area` (before resizing them to the original video resolution)
            "++model.fill_hole_area=8",
        ]
    if apply_long_term_memory:
        hydra_overrides_extra = hydra_overrides_extra.copy()
        hydra_overrides_extra += [
            "++model.use_long_term_memory=true",
            "++model.use_iou_to_find_long_mem=true",
            f"++model.num_long_mem_frame={num_long_mem_frame}",
        ]
    if num_cand_to_cond_frame != 1:
        hydra_overrides_extra += [
            f"++model.num_cand_to_cond_frame={num_cand_to_cond_frame}",
        ]
    hydra_overrides_extra = hydra_overrides_extra.copy()
    if apply_credible_initial_frame:
        hydra_overrides_extra += ["++model.use_credible_initial_frame=true"]
    else:
        hydra_overrides_extra += ["++model.use_credible_initial_frame=false"]
    if num_cifs_candidate_frame != 5:
        hydra_overrides_extra += [
            f"++model.num_cifs_candidate_frame={num_cifs_candidate_frame}",
        ]
    hydra_overrides.extend(hydra_overrides_extra)

    # Read config and init model
    cfg = compose(config_name=config_file, overrides=hydra_overrides)
    OmegaConf.resolve(cfg)
    if training_config_file != "":
        print("overriding model config with training config, and assuring that the template config has the same structure as the training config")
        training_cfg = compose(config_name=training_config_file)
        cfg_resolved = OmegaConf.to_container(training_cfg, resolve=False)
        cfg_resolved = OmegaConf.create(cfg_resolved)
        model_cfg = cfg_resolved.trainer.model
        for k, v in model_cfg.items():
            if k in cfg.model:
                if '_target_' in k:
                    continue
                cfg.model[k] = v

        pass


    model = instantiate(cfg.model, _recursive_=True)
    _load_checkpoint(model, ckpt_path, strict=strict_loading)
    model = model.to(device)
    if mode == "eval":
        model.eval()
    return model


def _hf_download(model_id):
    from huggingface_hub import hf_hub_download

    config_name, checkpoint_name = HF_MODEL_ID_TO_FILENAMES[model_id]
    ckpt_path = hf_hub_download(repo_id=model_id, filename=checkpoint_name)
    return config_name, ckpt_path


def build_sam2_hf(model_id, **kwargs):
    config_name, ckpt_path = _hf_download(model_id)
    return build_sam2(config_file=config_name, ckpt_path=ckpt_path, **kwargs)


def build_sam2_video_predictor_hf(model_id, **kwargs):
    config_name, ckpt_path = _hf_download(model_id)
    return build_sam2_video_predictor(
        config_file=config_name, ckpt_path=ckpt_path, **kwargs
    )


def _load_checkpoint(model, ckpt_path, strict=True):
    if ckpt_path is not None:
        sd = torch.load(ckpt_path, map_location="cpu", weights_only=True)["model"]
        missing_keys, unexpected_keys = model.load_state_dict(sd, strict=strict)
        if not strict:
            # print(f"Missing keys: {missing_keys}")
            for k in missing_keys:
                if 'language_model' not in k:
                    RuntimeError()
                    print(f"Missing key: {k}")
            if unexpected_keys:
                RuntimeError()
                print(f"Unexpected keys: {unexpected_keys}")
            return
        if missing_keys:
            logging.error(missing_keys)
            raise RuntimeError()
        if unexpected_keys:
            logging.error(unexpected_keys)
            raise RuntimeError()
        logging.info("Loaded checkpoint sucessfully")
